    private void tblTedarikcilerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTedarikcilerMouseClicked

        int row = tblTedarikciler.getSelectedRow();
        if (row > -1) {
            seciliID = bidLs.get(row);
            try {
                String q = "select*from tedarikciler where tedarikciId ='" + seciliID + "'";
                ResultSet rs = db.baglan().executeQuery(q);

                if (rs.next()) {
                    txtBadi.setText(rs.getString(2));
                    txtByetkili.setText(rs.getString(3));
                    txtBtel.setText(rs.getString(4));
                    txtBfax.setText(rs.getString(5));
                    txtBcep.setText(rs.getString(6));
                    txtBeposta.setText(rs.getString(7));
                    txtBBakiye.setText(rs.getString(8));
                    txtBvergid.setText(rs.getString(9));
                    txtBVergiN.setText(rs.getString(10));
                    txtBadres.setText(rs.getString(11));
                    ilId = rs.getInt(12) - 1;
                    cbBil.setSelectedIndex(rs.getInt(12) - 1);

                }
                grupModelDoldur();
            } catch (Exception e) {
                System.err.println("grup getirme hatası" + e);
            }

        }
    }//GEN-LAST:event_tblTedarikcilerMouseClicked

